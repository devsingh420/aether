// Helper functions
export function getProductsWithFarms(products = PRODUCTS) {
  return products.map(p => ({
    ...p,
    farm: FARMS[p.farmId] || {},
  }));
}

export function getFeaturedProducts() {
  return getProductsWithFarms()
    .filter(p => p.grade === 'A' && p.farm.verified)
    .sort((a, b) => (b.farm.rating || 0) - (a.farm.rating || 0))
    .slice(0, 4)
    .map(p => ({ ...p, featured: true }));
}

export function getHotSaleProducts() {
  // Products with high stock that need to move
  return getProductsWithFarms()
    .filter(p => p.stock > 2000)
    .map(p => ({
      ...p,
      urgentSale: true,
      discount: 15 + Math.floor(Math.random() * 10),
      daysLeft: 1 + Math.floor(Math.random() * 2),
    }))
    .slice(0, 6);
}

export function searchProducts(query) {
  const q = query.toLowerCase();
  return getProductsWithFarms().filter(p =>
    p.name.toLowerCase().includes(q) ||
    p.farm.name?.toLowerCase().includes(q) ||
    p.cat.toLowerCase().includes(q)
  );
}
